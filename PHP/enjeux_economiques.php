                    Ces valeurs sont soumises au principe de l’offre et
                    de la demande. L’offre de crypto-monnaies, c’est-à-dire leurs quantités, qui elles sont
                    limitées (21 millions de tokens pour le Bitcoin, par exemple) dépend de l’algorithme
                    associé à chacune d’elle. La demande quant à elle, dépend du nombre d’utilisateurs
                    et du nombre de transactions. Avec une offre quasi-statique, si la demande
                    augmente, la valeur de la crypto-monnaie s'accroît. Et inversement, si la demande
                    diminue, la valeur diminue. Avec une telle volatilité, les crypto-monnaies ne peuvent donc pas être considérées
                    comme des unités de compte. 

               

                
                <h2>Réserve de valeur</h2>  
                
                    Une réserve de valeur suppose que la monnaie peut être épargnée et ainsi prendre
                    de la valeur dans le temps. Malgré leur capacité à être stockées, les crypto-monnaies
                    ne prennent pas de la valeur quand elles le sont. Ainsi, le seul moyen de faire prendre
                    de la valeur aux crypto-monnaies, c’est la spéculation. Celles-ci ne peuvent donc ne
                    pas être considérés comme des réserves de valeur.
                


        </article>
        
        <article class="article">Comme elles ne respectent que partiellement ses trois fonctions économiques, les
            crypto-monnaies ne peuvent pas être considérées comme des monnaies. Leur utilisation
            comme moyen de paiement est donc limitée. De plus, elles ne représentent que 0,5 % de la
            monnaie en circulation.
        </article>

