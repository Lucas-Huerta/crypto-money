        <h1>Enjeux économiques des crypto-monnaies</h1>
        <article class="article">Une unité de crypto-monnaie est appelée “token” ou “jeton” en français. </article>
        <img class="img-responsive center-block" src="../IMG/tokens.png" alt="tokens"/>
        <h1>Le principe d'ICO</h1>
        <article class="article"> Pour créer
            une nouvelle crypto-monnaie, on organise des ICOs (Initial Coin Offering), ou introduction sur
            le marché qui sont des levées de fonds. Le principe est le même que les IPOs : financer le
            lancement et le développement d’un projet blockchain en offrant en retour aux investisseurs
            des tokens. Les tokens sont achetés à l’aide d’autres crypto-actifs, tels que le Bitcoin,
            l’Ethereum ou encore Neo. La seule différence entre les IPOs et les ICOs est que ces
            dernières sont accessibles à tous et toutes et sont généralement très peu voir pas du tout
            réglementée. Cependant, certains pays (comme la Chine ou encore les États-Unis) interdisent
            aux particuliers de prendre par à ces levées. C’est comme ça qu’en 2014, les fondateurs de
            l’Ethereum ont lancés une ICO dont les fonds (un peu plus de 18 millions de dollars) sont
            allés à la fondation Ethereum afin de développer la blockchain éponyme.
        </article>

        
        <img class="img-responsive center-block" src="../IMG/ico1.png" alt="schema ico"/>
        

        <article> Le schéma ci-dessus résume bien le principe de l'ICO : Des investisseurs financent le projet avec d'autres crypto-devises, qui sont, par l'ICO, échangées avec de nouveaux Tokens.</article>

        <h1>Les Crypto-monnaies sont-elles vraiement des moyens de paiements reconnus ?</h1>
        <article>Pour qu’une crypto-monnaie soit acceptée comme moyen de paiement, elle doit
            respecter trois fonctions économiques : intermédiaire des échanges, unité de compte, et
            réserve de valeur. Les crypto-monnaies actuelles ne remplissent que partiellement ces trois
            fonctions :
            
                <h2>Intermédiaire des échanges</h2>
                    Les crypto-monnaies n’ont pas de cours légal en France.
                    Même si les transactions avec ces monnaies sont possibles, elles peuvent êtres
                    refusées. Les crypto-monnaies ne respectent donc que partiellement cette fonction
                    d’intermédiaire des échanges.
                

                <h2>Unité de compte</h2>
                
                    Les crypto-monnaies sont des monnaies “décentralisées”, c’est-à-dire qu’elles ne sont
                    régulées par aucun État ou banque, contrairement aux monnaies courantes (comme
                    l’Euro) qui voient leur quantité de monnaie disponible ajustée selon la demande,
                    pour éviter que le cours de la monnaie augmente ou baisse brutalement. De ce fait,
                    les crypto-monnaies sont dites “volatiles”, leurs valeurs peuvent changer à tout
                    instant de façon très significative, comme le montre le tableau ci dessous :
            
            
